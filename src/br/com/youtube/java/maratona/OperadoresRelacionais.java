package br.com.youtube.java.maratona;

public class OperadoresRelacionais {
	public static void main(String[] args) {
		int var1 = 27;
		int var2 = 74;
		System.out.println("var1 = " + var1);
		System.out.println("var2 = " + var2);
		System.out.println("var1 == var2 -> " + (var1 == var2));
		System.out.println("var1 != var2 -> " + (var1 != var2));
		System.out.println("var1 < var2 -> " + (var1 < var2));
		System.out.println("var1 > var2 -> " + (var1 > var2));
		System.out.println("var1 <= var2 -> " + (var1 <= var2));
		System.out.println("var1 >= var2 -> " + (var1 >= var2));
	}

}
